#include <iostream>
using namespace std ;

int main()
{
	int initial_v, accelaration;
	 
	cout<<"Enter initial velocity(m/s): "; cin>>initial_v; 
	cout<<endl;
	cout<<"Enter accelaration(m/s^2):   "; cin>>accelaration;
	cout<<endl;
	cout<<"Final velocity will be(m/s): "<<(initial_v + accelaration)<<" m/s"<<endl<<endl; 
	cout<<endl;
	system("PAUSE") ;
	return 0 ;
}
	

