#include <iostream>
using namespace std ;

int main()
{
	float distance,time,speed;
	cout<<"Enter distance in (m): "; cin>>distance;
	cout<<endl; 
	cout<<"Enter  time in (sec): "; cin>>time; 
	cout<<endl;
	speed=(distance/time); 
	cout<<"Speed is equal to= "<<speed<<" m/s"<<endl<<endl;
		
	
	system("PAUSE") ;
	return 0 ;
}
	

